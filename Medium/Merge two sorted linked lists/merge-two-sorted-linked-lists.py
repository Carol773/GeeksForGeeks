def sortedMerge(head1, head2):
    # code here
    cur1=head1
    cur2=head2
    
    res1=[]
    res2=[]
    
    while cur1!=None:
        res1.append(cur1.data)
        cur1=cur1.next
    
    while cur2!=None:
        res2.append(cur2.data)
        cur2=cur2.next
        
    res1=sorted(res1)
    res2=sorted(res2)
    
    res=res1+res2
    res=sorted(res)
    
    dummy=Node(0)
    cur=dummy
    
    for i in res:
        
        cur.next=Node(i)
        cur=cur.next
        
    return dummy.next


